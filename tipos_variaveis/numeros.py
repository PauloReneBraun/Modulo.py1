#Inteiro 
numero_inteiro = 5 
print("Inteiro = ", numero_inteiro)

# Real com ponto flutuante 
numero_real = 5.17
print("Real com ponto flutuante = ", numero_real)

# type()
print("Tipo da variavel interiro = ", type(numero_inteiro))
print("Tipo da variavel real = ", type(numero_real))

# soma + 
soma = 1 + 1
print("1 + 1 =", soma)

# subtracao 
subtracao = 1 -1 
print("1 - 1 =", subtracao)


# multiplicacao *
multiplicacao = 3 * 3 
print("3 X 3 =", multiplicacao)

# divisao / 
divisao = 8 / 2 
print("8 / 2 =", divisao)
print("Tipo da variavel do resultado da divisao = ", type(divisao))
print("Valor Inteiro  = ", int(divisao))
